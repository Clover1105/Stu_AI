"""
元组
    - 用小括号()定义
    - 元素不可修改（不可变）
    - 可以存放任意类型数据
"""
# 创建元组
t1 = (1, 2, 3, 4)
t2 = ("Python", 3.14, True, [1, 2])
t3 = ()  # 空元组
print(f"整数元组：{t1}")
print(f"混合元组：{t2}")
print(f"空元组：{t3}")
# 单元素元组（必须有逗号）
d1 = (5,)
d2 = (5)  # 这是整数，不是元组
print(f"单元素元组：{d1}，类型：{type(d1)}")
print(f"没有逗号：{d2}，类型：{type(d2)}")
# 省略括号的元组
t = 10, 20, 30  # 元组打包
print(f"省略括号：{t}，类型：{type(t)}")