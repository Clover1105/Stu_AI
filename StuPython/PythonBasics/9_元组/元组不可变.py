"""
元组不可变性：
    - 元组创建后不能修改元素
    - 不能添加、删除元素
    - 但如果元素是可变对象，可以修改该对象的内部
元组不可变的实际含义：
    - 元组存储的是对象的引用（地址），这个引用不能改变
    - 但引用指向的对象本身可以改变
"""
# 尝试修改元组会报错
t = (10, 20, 30)
print(f"原元组：{t}")
# t[0] = 100  # 取消注释会报错：TypeError
# 修改元组中的列表元素
t = (10, [20, 30], 40)
print(f"修改前：{t}")
t[1][0] = 200  # 可以修改列表内部元素，列表是可变的
print(f"修改后：{t}")