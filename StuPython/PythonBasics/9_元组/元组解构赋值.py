"""
元组解构赋值
    - 将元组的元素按顺序赋值给多个变量
    - 变量数量必须与元素数量一致
    - 常用于交换变量值
# 1.先取出a变量的值 然后取出b变量的值
# 2.创建一个内存对象(元组类型的) 假设叫a --> (20,10)
# 3.把等号右边的数据容器(元组) 解构了 按照索引的顺序赋值给等号左边的变量
"""
# 基本解构
t = (100, 200, 300)
x, y, z = t  # x=100, y=200, z=300
print(f"x={x}, y={y}, z={z}")
# 交换变量值
a = 10
b = 20
print(f"交换前：a={a}, b={b}")
a, b = b, a  # 利用了元组打包和解构
print(f"交换后：a={a}, b={b}")
# 使用_忽略不需要的值
data = (1, 2, 3, 4, 5)
first, _, third, _, last = data
print(f"第一个：{first}，第三个：{third}，最后一个：{last}")