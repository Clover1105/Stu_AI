"""
集合：
    - 用花括号{}定义，或用set()创建
    - 元素无序、唯一（自动去重）
    - 不能使用索引访问
"""
# 创建集合
s1 = {1, 2, 3, 3, 4, 4, 5}  # 重复元素自动去重
print(f"集合自动去重：{s1}")
s2 = set()  # 空集合必须用set()，{}是空字典
print(f"空集合：{s2}")
s3 = set("hello")  # 字符串转集合，字符会去重
print(f"字符串转集合：{s3}")   # {'o', 'l', 'h', 'e'}

# 集合无序性
s = {5, 2, 8, 1, 9}
print(f"集合输出顺序不确定：{s}")

# 不能使用索引（字典是无序的，没有下标）
# print(s[0])  # 取消注释会报错：TypeError
print("集合不支持索引访问")