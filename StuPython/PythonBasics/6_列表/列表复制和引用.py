"""
列表复制
    - 直接赋值：引用相同内存
    - copy()：浅拷贝，创建新列表
引用数据的思想：
    列表是一个引用数据：它会创建列表对象，占用内存空间
    x = [10,20,30,40] -- 创建了一个列表对象，占用了一个内存空间
    y = x -- 不会重新创建一个列表对象，也就是说不会新开辟内存空间
    y[1] = 666
    print(y) -- [10,666,30,40]
"""
# 直接赋值（引用）
a = [1, 2, 3]
b = a
b[0] = 100
print(f"a：{a}")
print(f"b：{b}")
print(f"a和b是同一个对象：{a is b}")

# copy()复制
c = [1, 2, 3]
d = c.copy()
d[0] = 100
print(f"c：{c}")
print(f"d：{d}")
print(f"c和d是同一个对象：{c is d}")